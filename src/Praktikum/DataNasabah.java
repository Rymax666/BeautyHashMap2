package Praktikum;

public class DataNasabah {
    public int nope;
    public String nama;

    DataNasabah(String nama, int nope){
        this.nama = nama;
        this.nope = nope;
    }

    public String getNama() {
        return nama;
    }

    public int getNope() {
        return nope;
    }
}
